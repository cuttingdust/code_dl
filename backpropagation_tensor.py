"""单次反向传播示例：观察一次“前向传播→反向传播→参数更新”。

本例定义损失函数：

    L(w) = 2w²

它对参数 w 的导数为：

    dL/dw = 4w

当 w=10 时：

    loss = 2 × 10² = 200
    grad = 4 × 10 = 40

若学习率 lr=0.1，执行一次梯度下降：

    w_new = w_old - lr × grad
          = 10 - 0.1 × 40
          = 6

【单次反向传播适合什么场景？】

1. 初学 autograd，观察计算图、grad_fn、requires_grad 和 .grad。
2. 验证某个数学公式的梯度是否正确。
3. 调试模型的一次前向、反向过程，检查有没有梯度中断或异常梯度。
4. 演示一次参数更新前后的变化。

它只能展示“一步怎么走”，不能体现模型经过多轮训练后如何逐渐收敛。
"""

import torch


if __name__ == "__main__":
    # ==================================================================
    # 第 1 步：创建需要学习的参数 w0
    # ==================================================================
    # 10.0 是浮点数，因此张量默认可以参与浮点运算。
    # dtype=torch.float32 显式指定为常用的单精度浮点类型。
    # requires_grad=True 告诉 autograd：
    # “请记录所有由 w0 参与的运算，之后需要计算损失对 w0 的梯度。”
    #
    # w0 由我们直接创建，并不是其他张量计算出来的，所以它是叶子张量。
    # 反向传播后，叶子张量 w0 的梯度会累加到 w0.grad 中。
    w0 = torch.tensor(10.0, dtype=torch.float32, requires_grad=True)

    # ==================================================================
    # 第 2 步：前向传播，计算当前损失
    # ==================================================================
    # 执行这一行时，PyTorch 同时完成两件事：
    #
    # 1. 计算数值：2 × 10² = 200。
    # 2. 动态建立计算图：
    #
    #       w0 --平方--> w0² --乘以2--> loss
    #
    # loss 是由 w0 计算得到的非叶子张量，loss.grad_fn 记录了它最后一步
    # 运算的反向函数。反向传播会沿这张图从 loss 一直追溯到 w0。
    loss = 2 * w0**2

    print(f"w0 是否需要梯度：{w0.requires_grad}")
    print(f"loss 的反向函数：{loss.grad_fn}")
    print(f"更新前的权重 w0：{w0.item()}")
    print(f"前向传播得到的损失 loss：{loss.item()}")

    # ==================================================================
    # 第 3 步：反向传播，计算 loss 对 w0 的梯度
    # ==================================================================
    # backward() 从最终损失开始，根据链式法则反向计算：
    #
    #       dL/dw = d(2w²)/dw = 4w
    #
    # w0=10，因此 w0.grad=40。
    #
    # 为什么这里保留 sum()？
    # - 当前 loss 是零维标量，loss.sum() 仍然等于 loss，不改变计算结果。
    # - 如果以后 w0 变成向量或矩阵，loss 也可能包含多个元素；sum() 会先把
    #   所有元素相加为一个标量，这样就可以调用不带参数的 backward()。
    #
    # 注意：sum() 表示各项损失相加，mean() 表示取平均值，它们会产生不同
    # 大小的梯度。真实训练应根据损失函数的定义选择，而不是无条件使用 sum()。
    loss.sum().backward()

    # backward() 负责“算出梯度”，但不会自动修改参数。
    print(f"loss 对 w0 的梯度 w0.grad：{w0.grad.item()}")

    # ==================================================================
    # 第 4 步：使用梯度下降执行一次参数更新
    # ==================================================================
    # 梯度指向损失上升最快的方向。为了减小损失，需要沿梯度的反方向移动：
    #
    #       新参数 = 旧参数 - 学习率 × 梯度
    #
    # 学习率控制每一步走多远：过大可能跨过最低点甚至发散，过小则收敛很慢。
    lr = 0.1

    # 参数更新只是训练算法的操作，不需要继续对它求导，所以放进 no_grad()。
    # no_grad() 只关闭这个代码块内部的计算图记录，离开代码块后会自动恢复。
    #
    # 这里使用 w1 = ... 创建一个新张量，故意保留旧的 w0 进行前后对照。
    # 这是单次演示。如果需要继续训练多轮，就不能每轮都把参数变量换成一个
    # requires_grad=False 的新张量，而应像多次示例那样原地执行 w0 -= ...。
    with torch.no_grad():
        w1 = w0 - lr * w0.grad

    print(f"一次梯度下降后的权重 w1：{w1.item()}")
    print(f"原参数 w0 仍然保持不变：{w0.item()}")
    print(f"no_grad 中生成的 w1 是否需要梯度：{w1.requires_grad}")

    # ==================================================================
    # 补充：为什么单次示例没有清零梯度？
    # ==================================================================
    # 因为这里只调用了一次 backward()，w0.grad 之前是 None，不存在旧梯度。
    # PyTorch 的梯度默认采用“累加”机制；如果还要调用第二次 backward()，就要
    # 先执行 w0.grad.zero_()。多次反向传播示例会展示这个步骤。
