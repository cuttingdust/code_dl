"""多次反向传播示例：用训练循环让参数逐步接近损失函数的最低点。

本例仍然使用：

    L(w) = 2w²
    dL/dw = 4w

该函数的最低点是 w=0、L=0。初始参数设为 w=20，每一轮都执行：

    1. 清除上一轮梯度；
    2. 前向传播，使用当前 w 重新计算 loss；
    3. 反向传播，计算当前 loss 对 w 的梯度；
    4. 梯度下降，原地更新 w；
    5. 进入下一轮，使用更新后的 w 建立一张新的计算图。

【多次反向传播有什么好处？】

一次更新通常不可能直接找到最佳参数。多次更新能够不断用当前误差修正参数，
使损失逐渐下降、参数逐渐接近最优值。这就是模型“训练”的基本过程。

【适用场景】

1. 神经网络的正式训练，需要遍历许多批次和 epoch。
2. 线性回归、逻辑回归等模型的参数优化。
3. 观察损失是否下降、模型是否收敛，以及学习率是否合适。
4. 演示梯度累加、梯度清零和多轮参数更新之间的关系。
"""

import torch


if __name__ == "__main__":
    # ==================================================================
    # 第 1 步：创建初始可训练参数
    # ==================================================================
    # w0 是叶子张量，并且 requires_grad=True。
    # 多轮训练中始终原地更新这个张量，不能把 w0 重新绑定为 no_grad() 中
    # 创建的新张量，否则新的张量 requires_grad=False，下一轮就无法反向传播。
    w0 = torch.tensor(20.0, dtype=torch.float32, requires_grad=True)

    # epochs 表示训练轮数。这里的 100 只是为了清楚看到参数逐渐接近 0。
    epochs = 100

    # 本示例使用固定学习率，所以放在循环外，避免每轮重复赋相同的值。
    # 正式训练中学习率可以变化，通常由学习率调度器 scheduler 管理；也可以
    # 在循环内根据 epoch 计算，例如 lr = initial_lr * 0.95**epoch。
    lr = 0.1

    # ==================================================================
    # 第 2 步：开始多轮训练
    # ==================================================================
    for epoch in range(epochs):
        # --------------------------------------------------------------
        # 2.1 清除上一轮的梯度
        # --------------------------------------------------------------
        # PyTorch 默认把新梯度累加到 .grad，而不是自动覆盖旧梯度。
        # 假设第一轮梯度是 80，第二轮梯度是 48：如果不清零，第二轮的
        # w0.grad 会变成 80+48=128，从而使用错误的梯度更新参数。
        #
        # 第一轮开始时 w0.grad 是 None，因此先判断再清零。
        # 清零只会修改梯度缓冲区，不会修改参数 w0 的数值。
        if w0.grad is not None:
            w0.grad.zero_()

        # --------------------------------------------------------------
        # 2.2 前向传播：根据当前参数重新计算损失
        # --------------------------------------------------------------
        # 每一轮的 w0 都不同，所以必须重新计算 loss，并重新建立计算图。
        # 例如第一轮 w0=20、loss=800；更新后 w0=12，第二轮 loss=288。
        loss = 2 * w0**2

        # --------------------------------------------------------------
        # 2.3 反向传播：计算当前这一轮的梯度
        # --------------------------------------------------------------
        # 当前 loss 是标量，sum() 不改变其值；保留 sum() 后，如果以后参数
        # 改成向量或矩阵，也能先将多元素损失聚合成标量再反向传播。
        # 但真实任务中仍需根据损失定义选择 sum() 或 mean()。
        loss.sum().backward()

        # 保存更新前的参数、损失和梯度，用于输出和观察。
        # detach() 将副本与计算图分离，clone() 创建独立内存，保证后面原地
        # 修改 w0 时 old_w 不会跟着变化。
        old_w = w0.detach().clone()
        old_loss = loss.detach().clone()
        grad = w0.grad.detach().clone()

        # --------------------------------------------------------------
        # 2.4 梯度下降：原地更新同一个参数 w0
        # --------------------------------------------------------------
        # no_grad() 表示参数更新过程不需要加入计算图，否则 autograd 会继续
        # 记录“减去学习率×梯度”这个操作，产生无用计算图并增加内存开销。
        #
        # 必须使用原地更新：
        #
        #     w0 -= lr * w0.grad        正确：修改原叶子张量，仍可继续求梯度
        #     w0 = w0 - lr * w0.grad    错误：在 no_grad 中生成不求梯度的新张量
        #
        # 原地更新后，w0.requires_grad 仍然是 True，下一轮可以继续建立计算图。
        with torch.no_grad():
            w0 -= lr * w0.grad

        print(
            f"第 {epoch + 1:3d} 次计算，"
            f"损失值 {old_loss.item():.10f}，"
            f"梯度 {grad.item():.10f}，"
            f"原始权重 {old_w.item():.10f}，"
            f"更新后权重 {w0.item():.10f}"
        )

    # ==================================================================
    # 第 3 步：训练结束后检查结果
    # ==================================================================
    # L=2w² 的理论最优点是 w=0。训练结束后 w0 应已经非常接近 0，
    # 最终损失也应非常接近 0，但受浮点数精度影响不一定恰好等于 0。
    final_loss = 2 * w0.detach() ** 2

    print("-" * 80)
    print(f"训练结束后的参数：{w0.item():.12e}")
    print(f"训练结束后的损失：{final_loss.item():.12e}")
